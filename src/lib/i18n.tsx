const dicts: Record<Lang, Dict> = { en, pt };

type Ctx = { lang: Lang; setLang: (l: Lang) => void; t: (k: string) => string; toggle: () => void };
const I18nContext = createContext<Ctx | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");

  useEffect(() => {
    const saved = (typeof window !== "undefined" && (localStorage.getItem("aurum.lang") as Lang)) || "en";
    setLangState(saved === "pt" ? "pt" : "en");
  }, []);

  const setLang = (l: Lang) => {
    setLangState(l);
    if (typeof window !== "undefined") localStorage.setItem("aurum.lang", l);
    if (typeof document !== "undefined") document.documentElement.lang = l;
  };

  const t = (k: string) => dicts[lang][k] ?? dicts.en[k] ?? k;
  const toggle = () => setLang(lang === "en" ? "pt" : "en");

  return <I18nContext.Provider value={{ lang, setLang, t, toggle }}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used inside I18nProvider");
  return ctx;
}
